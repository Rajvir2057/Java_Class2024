public class Typecasting{
    public static void main(String[] args){
        // widening casting..
        int num = 5;
        double mydouble = num;
        System.out.println(mydouble);

        // manual casting...
        char asci = 65;
        int num2 = (int)asci;
        System.out.println(num2);

        // in the print 
        int num3 = 45;
        System.out.println((double)num3);
    }

}